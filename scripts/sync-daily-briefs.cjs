/**
 * 每日早参数据同步脚本
 * 构建前自动扫描 public/每日早参/ 目录，生成早参列表数据
 */

/* eslint-disable @typescript-eslint/no-require-imports */
const fs = require('fs');
const path = require('path');

const SOURCE_DIR = path.resolve(__dirname, '../public/每日早参');
const OUTPUT_FILE = path.resolve(__dirname, '../src/data/dailyBriefs.ts');

function parseDateFromFilename(filename) {
  // 文件名格式：article_YYYYMMDD.html
  const match = filename.match(/article_(\d{4})(\d{2})(\d{2})\.html$/);
  if (match) {
    return `${match[1]}-${match[2]}-${match[3]}`;
  }
  return null;
}

function formatDisplayDate(dateStr) {
  // 2026-04-13 -> 4月13日
  const [, month, day] = dateStr.split('-');
  return `${parseInt(month)}月${parseInt(day)}日`;
}

function getWeekday(dateStr) {
  // 获取星期几
  const date = new Date(dateStr);
  const weekdays = ['周日', '周一', '周二', '周三', '周四', '周五', '周六'];
  return weekdays[date.getDay()];
}

function syncDailyBriefs() {
  console.log('\u25fc \u6b63\u5728\u540c\u6b65\u6bcf\u65e5\u65e9\u53c2\u6570\u636e...');

  // 确保源目录存在
  if (!fs.existsSync(SOURCE_DIR)) {
    console.log('\u26a0\ufe0f  \u6bcf\u65e5\u65e9\u53c2\u76ee\u5f55\u4e0d\u5b58\u5728\uff0c\u8df3\u8fc7\u540c\u6b65');
    fs.writeFileSync(OUTPUT_FILE, '// Auto-generated - no daily briefs found\nexport interface DailyBrief {\n  date: string;\n  title: string;\n  file: string;\n  displayDate: string;\n}\n\nexport const dailyBriefs: DailyBrief[] = [];\n');
    return;
  }

  // 读取所有 HTML 文件
  const files = fs.readdirSync(SOURCE_DIR)
    .filter(file => file.endsWith('.html') && /^article_\d{8}\.html$/.test(file))
    .sort()
    .reverse(); // 最新日期排在前面

  if (files.length === 0) {
    console.log('\u26a0\ufe0f  \u672a\u627e\u5230\u65e9\u53c2\u6587\u4ef6\uff0c\u8df3\u8fc7\u540c\u6b65');
    fs.writeFileSync(OUTPUT_FILE, '// Auto-generated - no daily briefs found\nexport interface DailyBrief {\n  date: string;\n  title: string;\n  file: string;\n  displayDate: string;\n}\n\nexport const dailyBriefs: DailyBrief[] = [];\n');
    return;
  }

  const briefs = files.map(file => {
    const dateStr = parseDateFromFilename(file);
    const filePath = path.join(SOURCE_DIR, file);
    const content = fs.readFileSync(filePath, 'utf-8');

    // 从 <title> 标签提取标题
    const titleMatch = content.match(/<title>(.*?)<\/title>/i);
    const title = titleMatch ? titleMatch[1].trim() : file.replace('.html', '');

    return {
      date: dateStr,
      title: title,
      file: `每日早参/${file}`,
      displayDate: `${formatDisplayDate(dateStr)} ${getWeekday(dateStr)}`
    };
  });

  // 生成 TypeScript 文件
  const output = `// Auto-generated by scripts/sync-daily-briefs.cjs
// \u26a0\ufe0f \u6b64\u6587\u4ef6\u7531\u6784\u5efa\u811a\u672c\u81ea\u52a8\u751f\u6210\uff0c\u8bf7\u52ff\u624b\u52a8\u4fee\u6539

export interface DailyBrief {
  date: string;
  title: string;
  file: string;
  displayDate: string;
}

export const dailyBriefs: DailyBrief[] = ${JSON.stringify(briefs, null, 2)};
`;

  fs.writeFileSync(OUTPUT_FILE, output);
  console.log(`\u2705 \u5df2\u540c\u6b65 ${briefs.length} \u7bc7\u6bcf\u65e5\u65e9\u53c2`);
  console.log(`   \u8f93\u51fa\u6587\u4ef6: ${OUTPUT_FILE}`);
}

// 执行同步
syncDailyBriefs();
